class Pair<A, B> {
  final A first;
  final B second;
  Pair(this.first, this.second);
}

class Triple<A, B, C> {
  final A first;
  final B second;
  final C third;
  Triple(this.first, this.second, this.third);
}

class Quadruple<A, B, C, D> {
  final A first;
  final B second;
  final C third;
  final D fourth;
  Quadruple(
    this.first,
    this.second,
    this.third,
    this.fourth,
  );
}
